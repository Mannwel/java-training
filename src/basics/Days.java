package basics;

public class Days {
	public static void main(String[] args) {
		
		// Execute different block of code based to the value of a condition
		String dayOfWeek = "Sunday";
		
		switch (dayOfWeek) {
			case "Monday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
			case "Tuesday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
			case "Wednesday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
			case "Thursday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
			case "Friday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
			case "Saturday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
			case "Sunday" : 
				System.out.println("Today is " + dayOfWeek);
				break;
		
		
		}
	}

}
